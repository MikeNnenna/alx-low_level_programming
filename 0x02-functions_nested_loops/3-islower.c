#include "main.h"
/**
 * _islower(int c)
 * @c: determining variable
 * main - uses inbuilt function
 * Return: 0 || 1 (successful)
 */

int _islower(int c)
{
	int x;

	int y;

	x = 1;

	y = 0;
	
	(void)c;

	{
		if ('c' >= 'a' && 'c' <= 'z')
		{
			return (x);
		}
		
		else
	{
		return (y);
	}
	}
}
